package id.its.api.spmi.model;

public class Pustakawan {

	private String jenjangTerakhir;
	private Integer jumlahPustakawan;
	
	public String getJenjangTerakhir() {
		return jenjangTerakhir;
	}
	public void setJenjangTerakhir(String jenjangTerakhir) {
		this.jenjangTerakhir = jenjangTerakhir;
	}
	public Integer getJumlahPustakawan() {
		return jumlahPustakawan;
	}
	public void setJumlahPustakawan(Integer jumlahPustakawan) {
		this.jumlahPustakawan = jumlahPustakawan;
	}
	
}