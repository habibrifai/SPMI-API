package id.its.api.spmi.model;

public class MahasiswaLulus {
	private double rataLamaStudi;
	private double rataIpk;
	private Integer jumlahLulusan;
	private double maxIpk;
	private double minIpk;
	
	public double getRataLamaStudi() {
		return rataLamaStudi;
	}
	public void setRataLamaStudi(double rataLamaStudi) {
		this.rataLamaStudi = rataLamaStudi;
	}
	public double getRataIpk() {
		return rataIpk;
	}
	public void setRataIpk(double rataIpk) {
		this.rataIpk = rataIpk;
	}
	public Integer getJumlahLulusan() {
		return jumlahLulusan;
	}
	public void setJumlahLulusan(Integer jumlahLulusan) {
		this.jumlahLulusan = jumlahLulusan;
	}
	public double getMaxIpk() {
		return maxIpk;
	}
	public void setMaxIpk(double maxIpk) {
		this.maxIpk = maxIpk;
	}
	public double getMinIpk() {
		return minIpk;
	}
	public void setMinIpk(double minIpk) {
		this.minIpk = minIpk;
	}
}
